import { apiFetch } from '/static/js/api.js';
import { showMessage } from '/static/js/ui.js';

const config = window.MANAGE_AI_DATA || {};
const offerings = Array.isArray(config.offerings) ? config.offerings : [];
const textbooks = Array.isArray(config.textbooks) ? config.textbooks : [];
const offeringMap = new Map(offerings.map((item) => [Number(item.id), item]));
const textbookMap = new Map(textbooks.map((item) => [Number(item.id), item]));

const elements = {
    form: document.getElementById('aiConfigForm'),
    offeringSelect: document.getElementById('aiOfferingSelect'),
    textbookSelect: document.getElementById('aiTextbookSelect'),
    regenerateBtn: document.getElementById('aiRegenerateBtn'),
    generateBtn: document.getElementById('aiGenerateBtn'),
    submitBtn: document.getElementById('aiConfigSubmitBtn'),
    systemPromptInput: document.getElementById('aiSystemPromptInput'),
    syllabusInput: document.getElementById('aiSyllabusInput'),
    classroomSummary: document.getElementById('aiClassroomSummary'),
    textbookSummary: document.getElementById('aiTextbookSummary'),
    materialList: document.getElementById('aiMaterialList'),
};

const state = {
    activeOfferingId: null,
    latestPayload: null,
    autoGenerated: false,
    generatedDrafts: {
        systemPrompt: '',
        syllabus: '',
    },
};

function buildLocalTextbookSummary(textbook) {
    if (!textbook) {
        return '当前课堂未绑定教材。';
    }
    const lines = [
        `教材名称：${textbook.title || '未命名教材'}`,
        `作者：${textbook.author_display || (textbook.authors || []).join('、') || '未填写作者'}`,
    ];
    if (textbook.publisher) lines.push(`出版社：${textbook.publisher}`);
    if (textbook.publication_year) lines.push(`出版年份：${textbook.publication_year}`);
    if (textbook.tags?.length) lines.push(`教材标签：${textbook.tags.join('、')}`);
    if (textbook.introduction) lines.push(`教材简介：${textbook.introduction}`);
    if (textbook.catalog_text) lines.push(`教材目录：${textbook.catalog_text}`);
    return lines.join('\n');
}

function renderMaterialList(materialNames) {
    if (!elements.materialList) return;
    if (!materialNames?.length) {
        elements.materialList.innerHTML = '<span class="academic-badge is-muted">暂无课堂材料</span>';
        return;
    }
    elements.materialList.innerHTML = materialNames.map((name) => `<span class="academic-badge">${escapeHtml(name)}</span>`).join('');
}

function renderContext(payload) {
    const textbook = getSelectedTextbook();
    if (elements.classroomSummary) {
        elements.classroomSummary.textContent = payload?.classroom_summary || '请选择课堂后加载。';
    }
    if (elements.textbookSummary) {
        elements.textbookSummary.textContent = textbook
            ? buildLocalTextbookSummary(textbook)
            : (payload?.textbook_summary || '当前课堂尚未加载教材信息。');
    }
    renderMaterialList(payload?.recent_material_names || []);
}

function escapeHtml(value) {
    return String(value ?? '')
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#39;');
}

function getSelectedOffering() {
    return offeringMap.get(Number(elements.offeringSelect?.value || 0)) || null;
}

function getSelectedTextbook() {
    return textbookMap.get(Number(elements.textbookSelect?.value || 0)) || null;
}

function generateDrafts() {
    const offering = getSelectedOffering();
    const payload = state.latestPayload || {};
    const textbook = getSelectedTextbook();
    const teacherName = String(config.teacherName || '任课教师');
    if (!offering) {
        return {
            systemPrompt: '',
            syllabus: '',
        };
    }

    const textbookSummary = textbook
        ? buildLocalTextbookSummary(textbook)
        : (payload.textbook_summary || '当前课堂未绑定教材。');
    const classroomSummary = payload.classroom_summary || [
        `课程名称：${offering.course_name || ''}`,
        `班级：${offering.class_name || ''}`,
        `学期：${offering.semester || '未设置'}`,
    ].join('\n');
    const materialSummary = (payload.recent_material_names || []).length
        ? `最近课堂材料：${payload.recent_material_names.join('、')}`
        : '最近课堂材料：暂无';

    return {
        systemPrompt: `你是《${offering.course_name}》课堂的 AI 助教，服务对象包括教师和学生。\n\n--- 课堂摘要 ---\n${classroomSummary}\n\n--- 教材摘要 ---\n${textbookSummary}\n\n--- 使用边界 ---\n1. 优先基于课堂摘要、教材摘要、课程大纲和课堂材料回答。\n2. 面向教师时，帮助其备课、设计活动、梳理知识点、优化课堂表达。\n3. 面向学生时，优先讲思路、举例和拆步骤，不直接代写作业或泄露考试答案。\n4. 若问题超出当前课程范围，明确说明边界，并给出继续查证的方向。\n5. 发现教材、课堂材料与大纲表述不一致时，先指出差异，再给出稳妥解释。\n6. 始终使用简体中文，表达清晰、具体、自然。\n\n--- 教学协同 ---\n1. 任课教师：${teacherName}。必要时可提醒教师结合本班实际节奏调整。\n2. 回答中适当引用章节、知识点、实验或材料名称，让建议可落地。\n3. 在回答学习问题外，如学生明显焦虑，先短句共情，再给出可执行建议。\n\n--- 当前材料快照 ---\n${materialSummary}`,
        syllabus: `课程名称：${offering.course_name}\n班级：${offering.class_name}\n学期：${offering.semester || '未设置'}\n教材：${textbook ? textbook.title : '未绑定教材'}\n\n一、课程目标\n- 请补充本课程在本学期的教学目标、能力目标和考核重点。\n\n二、章节与知识点\n- 第 1 章：\n- 第 2 章：\n- 第 3 章：\n\n三、教材对应关系\n${textbookSummary}\n\n四、课堂材料与实践\n${materialSummary}\n- 请补充实验、案例、阅读材料与课堂活动安排。\n\n五、考核方式\n- 平时\n- 作业\n- 实验\n- 期中 / 期末\n\n六、AI 回答约束\n- 哪些问题可以直接回答\n- 哪些问题需要引导学生回到教材或课堂材料\n- 哪些内容必须提醒教师亲自确认`,
    };
}

function applyDrafts(drafts) {
    state.generatedDrafts = drafts;
    elements.systemPromptInput.value = drafts.systemPrompt;
    elements.syllabusInput.value = drafts.syllabus;
    state.autoGenerated = true;
}

async function loadConfig(offeringId) {
    if (!offeringId) {
        state.activeOfferingId = null;
        state.latestPayload = null;
        elements.systemPromptInput.value = '';
        elements.syllabusInput.value = '';
        renderContext(null);
        return;
    }

    state.activeOfferingId = Number(offeringId);
    const payload = await apiFetch(`/api/manage/ai/config/${offeringId}`);
    state.latestPayload = payload;

    const textbookId = payload.textbook_id || Number(elements.offeringSelect.selectedOptions[0]?.dataset.textbookId || 0) || '';
    elements.textbookSelect.value = textbookId ? String(textbookId) : '';
    renderContext(payload);

    const hasStoredPrompt = Boolean(String(payload.system_prompt || '').trim());
    const hasStoredSyllabus = Boolean(String(payload.syllabus || '').trim());
    if (hasStoredPrompt) {
        elements.systemPromptInput.value = payload.system_prompt;
    }
    if (hasStoredSyllabus) {
        elements.syllabusInput.value = payload.syllabus;
    }

    if (!hasStoredPrompt || !hasStoredSyllabus) {
        const drafts = generateDrafts();
        state.generatedDrafts = drafts;
        if (!hasStoredPrompt) elements.systemPromptInput.value = drafts.systemPrompt;
        if (!hasStoredSyllabus) elements.syllabusInput.value = drafts.syllabus;
        state.autoGenerated = !hasStoredPrompt && !hasStoredSyllabus;
    } else {
        state.autoGenerated = false;
    }
}

async function handleSubmit(event) {
    event.preventDefault();
    if (!elements.form || !elements.submitBtn) return;

    const offering = getSelectedOffering();
    if (!offering) {
        showMessage('请先选择一个课堂', 'warning');
        return;
    }

    const formData = new FormData(elements.form);
    const originalText = elements.submitBtn.textContent;
    elements.submitBtn.disabled = true;
    elements.submitBtn.textContent = '正在保存...';

    try {
        const result = await apiFetch(elements.form.action, {
            method: 'POST',
            body: formData,
        });
        showMessage(result.message || 'AI 配置已保存', 'success');
        state.autoGenerated = false;
    } catch (error) {
        showMessage(error.message || 'AI 配置保存失败', 'error');
    } finally {
        elements.submitBtn.disabled = false;
        elements.submitBtn.textContent = originalText;
    }
}

function bindEvents() {
    if (!elements.form) return;

    elements.offeringSelect?.addEventListener('change', async (event) => {
        const offeringId = Number(event.target.value || 0);
        if (!offeringId) {
            await loadConfig(null);
            return;
        }
        try {
            await loadConfig(offeringId);
        } catch (error) {
            showMessage(error.message || '加载课堂 AI 配置失败', 'error');
        }
    });

    elements.textbookSelect?.addEventListener('change', () => {
        renderContext(state.latestPayload);
        if (state.autoGenerated) {
            applyDrafts(generateDrafts());
        }
    });

    [elements.systemPromptInput, elements.syllabusInput].forEach((textarea) => {
        textarea?.addEventListener('input', () => {
            state.autoGenerated = false;
        });
    });

    elements.regenerateBtn?.addEventListener('click', () => {
        const offering = getSelectedOffering();
        if (!offering) {
            showMessage('请先选择一个课堂，再生成默认草稿', 'warning');
            return;
        }
        applyDrafts(generateDrafts());
        showMessage('已根据当前课堂与教材重新生成默认草稿', 'success');
    });

    elements.generateBtn?.addEventListener('click', async () => {
        const offering = getSelectedOffering();
        if (!offering) {
            showMessage('请先选择一个课堂，再使用 AI 生成', 'warning');
            return;
        }
        const textbook = getSelectedTextbook();
        if (!textbook) {
            showMessage('请先选择一本教材，AI 生成需要教材信息作为知识依据', 'warning');
            return;
        }

        const originalText = elements.generateBtn.textContent;
        elements.generateBtn.disabled = true;
        elements.generateBtn.textContent = '正在生成中...';

        try {
            const result = await apiFetch('/api/manage/ai/ai-generate', {
                method: 'POST',
                body: {
                    class_offering_id: offering.id,
                    textbook_id: textbook.id,
                },
            });
            if (result.system_prompt) {
                elements.systemPromptInput.value = result.system_prompt;
            }
            if (result.syllabus) {
                elements.syllabusInput.value = result.syllabus;
            }
            state.autoGenerated = true;
            showMessage('AI 已生成系统提示词和课程大纲，请检查并按需调整', 'success');
        } catch (error) {
            showMessage(error.message || 'AI 生成失败，请稍后重试', 'error');
        } finally {
            elements.generateBtn.disabled = false;
            elements.generateBtn.textContent = originalText;
        }
    });

    elements.form.addEventListener('submit', handleSubmit);
}

async function init() {
    if (!elements.form) return;
    bindEvents();

    if (offerings.length > 0 && elements.offeringSelect && !elements.offeringSelect.value) {
        elements.offeringSelect.selectedIndex = 1;
    }

    const initialOfferingId = Number(elements.offeringSelect?.value || 0);
    if (initialOfferingId) {
        try {
            await loadConfig(initialOfferingId);
        } catch (error) {
            showMessage(error.message || '加载课堂 AI 配置失败', 'error');
        }
    }
}

init();
