export const UNICODE_EMOJI_MAP = new Map(
    EMOJI_CATEGORIES.flatMap((category) => category.emojis.map((emoji) => [emoji.char, emoji]))
);

export function getEmojiMeta(char) {
    return UNICODE_EMOJI_MAP.get(char) || null;
}

export function buildTwemojiUrl(code) {
    const normalizedCode = String(code || '').replace(/-fe0f$/i, '');
    return `/static/vendor/twemoji/svg/${normalizedCode}.svg`;
}
